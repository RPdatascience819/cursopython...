# Exercícios
# Crie funções que duplicam, triplicam e quadruplicam o número recebido como parâmetro

def duplicar(n):
    return n * 2

def triplicar(n):
    return n * 3

def quadriplicar(n):
    return n * 4

num = 5

print(f'O dobro de {num} é {duplicar(num)}')
print(f'O triplo de {num} é {triplicar(num)}')
print(f'O quadruplo de {num} é {quadriplicar(num)}')